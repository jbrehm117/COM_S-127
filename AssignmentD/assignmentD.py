# Assignment Operators Demo


# += demo
x = int(input("input an integer:"))
print("The value of x is: {0}".format(x))
x += 5
print("The value of x is now {0}".format(x))

# -= demo
x = int(input("input an integer:"))
print("The value of x is: {0}".format(x))
x -= 5
print("The value of x is now {0}".format(x))

# *= demo
x = int(input("input an integer:"))
